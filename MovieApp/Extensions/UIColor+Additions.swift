import UIKit

extension UIColor {
    @nonobjc class var borderColor: UIColor {
        return UIColor(red: 112.0 / 255.0, green: 112.0 / 255.0, blue: 112.0 / 255.0, alpha: 0.3)
    }
    
    @nonobjc class var greyColor: UIColor {
        return UIColor(red: 135.0 / 255.0, green: 134.0 / 255.0, blue: 132.0 / 255.0, alpha: 1)
    }
    
}

